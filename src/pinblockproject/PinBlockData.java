package pinblockproject;

/**
 *
 * @author danidu_o
 */
public class PinBlockData {
    private String cardNum;
    private String pin;
    private String twk;
    private String c1;
    private String c2;
    private String c3;
    
    public String getCardNum() {
        return cardNum;
    }

    public void setCardNum(String cardNum) {
        this.cardNum = cardNum;
    }

    public String getPin() {
        return pin;
    }

    public void setPin(String pin) {
        this.pin = pin;
    }

    public String getTwk() {
        return twk;
    }

    public void setTwk(String twk) {
        this.twk = twk;
    }

    public String getC1() {
        return c1;
    }

    public void setC1(String c1) {
        this.c1 = c1;
    }

    public String getC2() {
        return c2;
    }

    public void setC2(String c2) {
        this.c2 = c2;
    }

    public String getC3() {
        return c3;
    }

    public void setC3(String c3) {
        this.c3 = c3;
    }
    
}
